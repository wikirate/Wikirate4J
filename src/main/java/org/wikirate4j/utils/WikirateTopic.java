    public static WikirateTopic getWikirateTopic(String topic) {
        for (WikirateTopic wikirateTopic : values()) {
            if (wikirateTopic.value().equals(topic)) {
                return wikirateTopic;
            }
        }

        return UNKNOWN;
    }
